def get_longest_register_name(register_list):
  longest_name = 0
  for reg in register_list:
    if len(reg) > longest_name:
      longest_name = len(reg)

  return longest_name


def print_registers(register_list, longest_name):
  address = 0

  for reg in register_list:
    print("#define {}    {}".format(
      reg.ljust(longest_name), "{0:#0{1}x}".format(address, 6)
    ))
    address += 0x04


